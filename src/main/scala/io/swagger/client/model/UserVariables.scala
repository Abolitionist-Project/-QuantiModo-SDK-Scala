  joinWith: String,
  /* maximumValue */
  maximumValue: Float,
  /* minimumValue */
  minimumValue: Float,
  /* name */
  name: String,
  /* onsetDelay */
  onsetDelay: Integer,
  /* unit */
  unit: String)
  
